    <h2>Ejemplos de Aprendizaje:</h2>

    <h3>1. Colores y aula:</h3>
    <p>
        Red, blue, yellow, green, black.<br>
        This is a red pencil. That is a blue notebook.<br>
        <em>Pronunciación:</em> /red/ — /blue/ — /yelow/
    </p>

    <h3>2. Estaciones del año:</h3>
    <p>
        Spring, summer, autumn (fall), winter.<br>
        I like summer because it’s hot.<br>
        <em>Pronunciación:</em> /sprin/ — /samer/ — /winter/
    </p>

    <h3>3. Familia:</h3>
    <p>
        My sister is 10 years old. My grandparents live in the countryside.<br>
        <strong>Español:</strong> Mi hermana tiene 10 años. Mis abuelos viven en el campo.
    </p>

    <h3>4. Clima:</h3>
    <p>
        It’s sunny today. It’s raining. It’s very cold in winter.<br>
        <em>Pronunciación:</em> /Its sani tudei/ — /Its reining/
    </p>

    <h3>5. Emociones:</h3>
    <p>
        I am happy. She is sad. We are excited!<br>
        <em>Pronunciación:</em> /aim japi/ — /shis sad/ 
    </p>

    <h3>6. Partes del cuerpo:</h3>
    <p>
        Head, arms, hands, legs, feet.<br>
        I have two arms and two legs.<br>
        <em>Pronunciación:</em> /ai jaf tu arms and tu legs/ 
    </p>

    <h3>7. Frases en clase:</h3>
    <p>
        Can you repeat, please? — Yes, of course.<br>
        What does “homework” mean? — It means “tarea”.<br>
        <em>Pronunciación:</em> /wat dus jomwork min?/ 
    </p>

    <h3>8. Actividades diarias:</h3>
    <p>
        I brush my teeth. I take the bus. I study English at night.<br>
        <strong>Español:</strong> Me cepillo los dientes. Tomo el bus. Estudio inglés por la noche.
    </p>

    <h3>9. Preposiciones:</h3>
    <p>
        The keys are on the table. The bag is under the chair.<br>
        <em>Pronunciación:</em> /da kei ar on da teibol/
    </p>

    <h3>10. Frases básicas con "live", "have", "like":</h3>
    <p>
        I live in Los Olivos. I have a red bike. I like reading books.<br>
        <strong>Español:</strong> Vivo en Los Olivos. Tengo una bicicleta roja. Me gusta leer libros.
    </p>
