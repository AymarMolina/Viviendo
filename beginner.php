    <h2>Ejemplos con Traducción y Pronunciación</h2>

    <h3>1. El Abecedario:</h3>
    <p>
        A (ei), B (bi), C (si), D (di), E (i), F (ef), G (yi), H (eich), I (ai), J (yei), K (kei), L (el), M (em), N (en), O (ou), P (pi), Q (kiu), R (ar), S (es), T (ti), U (iu), V (vi), W (dábl iu), X (eks), Y (guai), Z (zi).
    </p>

    <h3>2. Saludos:</h3>
    <p>
        <strong>Inglés:</strong> Hello! Good morning! How are you? (helóu, gud mórning, jáu ar yú?)<br>
        <strong>Español:</strong> ¡Hola! ¡Buenos días! ¿Cómo estás?
    </p>
    <p>
        <strong>Inglés:</strong> Good evening. Nice to meet you. (gud ívning, nais tu míit yú)<br>
        <strong>Español:</strong> Buenas noches. Encantado de conocerte.
    </p>

    <h3>3. Presentaciones:</h3>
    <p>
        <strong>Inglés:</strong> My name is Juan. I am 18 years old. I live in Los Olivos.<br>
        <strong>Pronunciación:</strong> (mai néim is Juán. Ai am éitein yirs óuld. Ai liv in Los Olivos)<br>
        <strong>Español:</strong> Me llamo Juan. Tengo 18 años. Vivo en Los Olivos.
    </p>

    <h3>4. Pronombres + Verbo "To Be":</h3>
    <p>
        <strong>I am a student.</strong> (ai am a stúdent) — Soy un estudiante<br>
        <strong>You are my friend.</strong> (yu ar mai frend) — Tú eres mi amigo<br>
        <strong>He is a doctor.</strong> (ji is a dóctor) — Él es doctor<br>
        <strong>She is happy.</strong> (shi is jápi) — Ella está feliz<br>
        <strong>It is a cat.</strong> (it is a kat) — Es un gato<br>
        <strong>We are ready.</strong> (wi ar rédi) — Estamos listos<br>
        <strong>They are students.</strong> (dei ar stúdents) — Ellos son estudiantes
    </p>

    <h3>5. Formas Negativas:</h3>
    <p>
        I am not sad. (ai am not sad) — No estoy triste<br>
        You are not late. (yu ar not leit) — No estás tarde<br>
        She is not at home. (shi is not jóum) — Ella no está en casa
    </p>

    <h3>6. Preguntas Básicas:</h3>
    <p>
        Are you happy? — Yes, I am. (¿Estás feliz? — Sí, lo estoy)<br>
        Is he your teacher? — No, he isn’t. (¿Él es tu profesor? — No, no lo es)
    </p>

    <h3>7. Números del 1 al 20:</h3>
    <p>
        One (wuan), Two (tu), Three (zrii), Four (for), Five (faiv), Six (siks), Seven (séven), Eight (eit), Nine (nain), Ten (ten)...<br>
        Eleven, Twelve, Thirteen, Fourteen, Fifteen, Sixteen, Seventeen, Eighteen, Nineteen, Twenty
    </p>

    <h3>8. Colores Básicos:</h3>
    <p>
        Red (red) — Rojo<br>
        Blue (blu) — Azul<br>
        Green (grin) — Verde<br>
        Yellow (iélou) — Amarillo<br>
