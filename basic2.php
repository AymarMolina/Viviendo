<section class="ejemplos">
    <h2>Ejemplos de Aprendizaje:</h2>

    <h3>1. Rutina diaria:</h3>
    <p>
        I wake up at 6:30. Then I brush my teeth, take a shower, and eat breakfast.<br>
        She goes to school at 8:00. We come back home at 5:00.<br>
        <strong>Pronunciación:</strong> ai weik ap / shi goz tu skul / wi com bak jom
    </p>

    <h3>2. Adverbios de frecuencia:</h3>
    <p>
        I always eat fruit in the morning.<br>
        He usually studies at night.<br>
        They sometimes go to the park.<br>
        She never drinks soda.<br>
        <strong>Pronunciación:</strong> ai olweys it frut / hi yushuali estadis / she nevr drinks sōda
    </p>

    <h3>3. Presente simple (forma):</h3>
    <p>
        Afirmativa: He plays football.<br>
        Negativa: She doesn't like coffee.<br>
        Interrogativa: Do you read books? — Yes, I do.<br>
        <strong>Pronunciación:</strong> hi pleis futbol / shi dosent laik cofi / du yu rid buks?
    </p>

    <h3>4. Vocabulario adicional:</h3>
    <p>
        <strong>Casa:</strong> kitchen, bathroom, bedroom, living room<br>
        <strong>Comida:</strong> bread, cheese, banana, orange juice<br>
        <strong>Ropa:</strong> jacket, skirt, hat, socks<br>
        <strong>Lugares:</strong> school, park, supermarket, hospital
    </p>

    <h3>5. Conectores:</h3>
    <p>
        I like rice and chicken.<br>
        She is tired but she wants to study.<br>
        He eats vegetables because they are healthy.<br>
        We can stay home or go out.<br>
        <strong>Pronunciación:</strong> ai laik rais and chiken / shi is taierd bat shi wants tu stadi
    </p>

    <h3>6. Verbos en tercera persona:</h3>
    <p>
        He walks to work. She cooks dinner. It rains a lot in April.<br>
        <strong>Pronunciación:</strong> hi woks tu work / shi cuks diner / it reins a lot in april
    </p>

    <h3>7. Pronombres posesivos:</h3>
    <p>
        This is my book. That is your pencil.<br>
        His name is John. Her car is red.<br>
        Our house is big. Their dog is small.<br>
        <strong>Pronunciación:</strong> dis is mai buk / dat is yor pensil / his neim is yon
    </p>

    <h3>8. Preposiciones de tiempo:</h3>
    <p>
        I go to school in the morning.<br>
        We have a class on Monday.<br>
        She wakes up at 6:00.<br>
        <strong>Pronunciación:</strong> ai go tu skul in de morning / wi jav a clas on mondei
    </p>

    <h3>9. Expresiones de tiempo:</h3>
    <p>
        I play soccer in the afternoon.<br>
        They watch TV at night.<br>
        <strong>Pronunciación:</strong> ai plei sóker in di afternun / dei woch ti vi at nait
    </p>

    <h3>10. Gustos y habilidades:</h3>
    <p>
        I like to dance. She likes to sing.<br>
        We can swim. He can play the guitar.<br>
        <strong>Pronunciación:</strong> ai laik tu dans / shi laiks tu sing / wi kan suim / hi kan plei de guitár
    </p>
